public class VowelSrv
{

	public int vowelCount(String s)
	{
		int length = 0, position = 0;
		String vowelSentence = " ";
		
		while(position < s.length())
		{
			char character = s.charAt(position);
				
			if(character == 'a' || character == 'A')
			{
				vowelSentence += character;	
			}
			if(character == 'e' || character == 'E')
			{
				vowelSentence += character;	
			}
			if(character == 'i' || character == 'I')
			{
				vowelSentence += character;	
			}
			if(character == 'o' || character == 'O')
			{
				vowelSentence += character;	
			}
			if(character == 'u' || character == 'U')
			{
				vowelSentence += character;	
			}	
			position++;
		}	
		
		length = vowelSentence.length()-1;
		
		return length;
	}
	
	public String vowelString(String s)
	{
		int position = 0;
		String vowelSentence = " ", none = "not listed";
		
		while(position < s.length())
		{
			char character = s.charAt(position);
				
			if(character == 'a' || character == 'A')
			{
				vowelSentence += character;	
			}
			if(character == 'e' || character == 'E')
			{
				vowelSentence += character;	
			}
			if(character == 'i' || character == 'I')
			{
				vowelSentence += character;	
			}
			if(character == 'o' || character == 'O')
			{
				vowelSentence += character;	
			}
			if(character == 'u' || character == 'U')
			{
				vowelSentence += character;	
			}	
			position++;
		}
		if(vowelSentence == " ")
		{
			return none;	
		}
		else
			return vowelSentence;
	}
}	
